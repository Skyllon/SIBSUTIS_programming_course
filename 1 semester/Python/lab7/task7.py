# Вы работаете над созданием новой системы управления паролями для небольшой компании. Вам
# нужно написать программу, которая будет генерировать пароли определённой сложности и длины
# по запросу пользователя. Программа должна учитывать различные критерии сложности пароля,
# предоставляя пользователю выбор, какие символы должны быть включены в пароль.
# Запрашивать у пользователя:
# • желаемую длину пароля (целое число);
# • нужны ли заглавные буквы (да/нет);
# • нужны ли строчные буквы (да/нет);
# • нужны ли цифры (да/нет);
# • нужны ли специальные символы (да/нет).
# Генерировать пароль:
# • На основе выбранных пользователем параметров формировать строку с символами.
# • Для генерации случайных символов использовать random.choice().

import random
import string

def main():
    length = int(input("Введите желаемую длину пароля: "))
    use_upper = input("Нужны ли заглавные буквы? (да/нет): ").strip().lower() == 'да'
    use_lower = input("Нужны ли строчные буквы? (да/нет): ").strip().lower() == 'да'
    use_digits = input("Нужны ли цифры? (да/нет): ").strip().lower() == 'да'
    use_special = input("Нужны ли специальные символы? (да/нет): ").strip().lower() == 'да'

    symbols = ''
    if use_upper:
        symbols += string.ascii_uppercase
    if use_lower:
        symbols += string.ascii_lowercase
    if use_digits:
        symbols += string.digits
    if use_special:
        symbols += string.punctuation

    if not symbols:
        print("Должен быть выбран хотя бы один тип символов.")
        return

    password = ''.join(random.choice(symbols) for _ in range(length))
    print("Сгенерированный пароль:", password)
    
main()