# Вы – разработчик программного обеспечения для спортивных новостных сайтов. 
# Вам поручено написать программу, которая автоматически определяет победителя 
# матча по результатам, выведенным на табло стадиона. На табло стадиона в конце
# игры горит надпись:
# Название 1-Название 2 A:B
# где Название 1 и Название 2 — названия двух команд, а A:B — счёт матча.
# Напишите программу, которая определяет, какая команда победила.
# Названия команд могут содержать любые символы (буквы, цифры, знаки), кроме «-» и «:».
# Символы «-» и «:» содержатся в строке ровно по одному разу.
# Программа должна вывести название победившей команды или ничью.

def determine_winner(match_result):
    try:
        teams_part, score_part = match_result.rsplit(' ', 1)
        team1, team2 = teams_part.split('-')
        score1, score2 = map(int, score_part.split(':'))

        if score1 > score2:
            return team1.strip()
        elif score2 > score1:
            return team2.strip()
        else:
            return "Ничья"
    
    except ValueError:
        return "Ошибка! Формат данных неправильный"

match_result = input("Введите результат матча: ")
print(determine_winner(match_result))
